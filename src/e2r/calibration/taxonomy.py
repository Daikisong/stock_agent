def _clean_identifier(value: Any) -> str | None:
    if value is None:
        return None
    text = str(value).strip().strip("`").strip().strip('"').strip("'").strip().rstrip(",").strip()
    if not text:
        return None
    return text.upper()


def normalise_large_sector_id(value: Any) -> str | None:
    text = _clean_identifier(value)
    if text is None:
        return None
    return LARGE_SECTOR_ALIASES.get(text, text)


def normalise_canonical_archetype_id(value: Any) -> str | None:
    text = _clean_identifier(value)
    if text is None:
        return None
    return CANONICAL_ARCHETYPE_ALIASES.get(text, text)


